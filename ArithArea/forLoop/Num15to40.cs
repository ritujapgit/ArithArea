using System;
using System.Collections.Generic;
using System.Text;

namespace ArithArea.forLoop
{
    class Num15to40
    {
        static void Main(string[] args)
        {
            Console.WriteLine("Display number between 15 to 40.");
            int i = 15;
            for (i = 15; i <= 40; i++)
            {
                Console.WriteLine(i);
            }
        }

    }
}
