using System;
using System.Collections.Generic;
using System.Text;

namespace ArithArea.forLoop
{
    class Number150to101
    {
        static void Main(string[]args)
        {
            Console.WriteLine("Display number between 150 to 101.");
            int i = 150;
            for (i = 150; i >= 101; i--)
            {
                Console.WriteLine(i);
            }
        }
    }
}
